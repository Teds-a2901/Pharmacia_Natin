                <br>Whether you are at home, work, or school, know where the first aid kit and the AED are kept and be familiar with their contents. Know how to activate the Emergency Medical Services (EMS) in your area. Be aware of any policies in the workplace regarding medical emergencies.
                <br>After determining the problem, the next step in providing help is to determine the responsiveness of the injured or ill person. The best way to determine this is to tap the person and talk loudly to them: “Are you okay?” After determining responsiveness, yell for help. Look for any medical identifications, such as a necklace or a bracelet. This may provide a valuable clue to the cause of the situation.
